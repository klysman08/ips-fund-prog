def main():
    PI = 3.14159
    raio = float(input("Raio (cm): "))
    area = PI * raio ** 2
    print("Área:", area)


# Esta linha fornecida é necessária no final de um ficheiro Python
# para chamar a função main().
if __name__ == '__main__':
    main()
